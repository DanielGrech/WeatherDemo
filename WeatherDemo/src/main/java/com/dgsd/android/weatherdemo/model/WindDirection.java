package com.dgsd.android.weatherdemo.model;

/**
 * Represents general directions of wind
 */
public enum WindDirection {
    /**
     * North
     */
    N,

    /**
     * North-east
     */
    NE,

    /**
     * East
     */
    E,

    /**
     * South-east
     */
    SE,

    /**
     * South
     */
    S,
    /**
     * South-west
     */
    SW,

    /**
     * West
     */
    W,

    /**
     * North-west
     */
    NW
}
